package ConstructorLargo;

public class ConstructorTradicional {
    // attributes
    private String marca;
    private String modelo;
    private String motor;
    private int puertas;

    // constructor
    public ConstructorTradicional(String marca, String modelo, String motor, int puertas) {
        this.marca = marca;
        this.modelo = modelo;
        this.motor = motor;
        this.puertas = puertas;
    }
}
